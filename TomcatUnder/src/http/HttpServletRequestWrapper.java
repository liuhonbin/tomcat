	// 构造方法/传入字节输入流

	public HttpServletRequestWrapper(InputStream is) {
		byte[] bt = new byte[1024];
		int length = 0;
		try {
			if ((length = is.read(bt)) > 0) {
				String msg = new String(bt, 0, length);
				// 把输入的字符串以“[\\s]”为条件分割成一个String数组
				String[] line = msg.split("\\s");
				this.method = line[0];
				this.url = line[1];
			}
		} catch (IOException e) {
			e.printStackTrace();
		}

	}

